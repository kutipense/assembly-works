QS PROC FAR
			; AX START, BX END
			PUSH AX
			PUSH BX
			;LEA SI,DIZI
			MOV SI,AX ; J
			MOV DI,BX ; LAST ELEMENT 
			
			XCHG AX,BX
			SUB AX,BX
			MOV CL,2
			DIV CL
			MOV CX,AX ; # OF LOOPS

			MOV BX,SI ; I

	LS:		MOV AX,[DI]
			CMP AX,[SI]
			JNA LELSE
				MOV AX,[SI]
				XCHG AX,[BX]
				MOV [SI],AX
				ADD BX,2
	LELSE:	ADD SI,2
			LOOP LS
			MOV AX,[DI]
			XCHG AX,[BX] ;BX is the pivot
			MOV [DI],AX 
			MOV DX,BX ; RETURN DX
			POP BX
			POP AX
			RETF
QS ENDP

SORT PROC FAR
			; AX START, BX END
			PUSH DX
			PUSH AX
			PUSH BX
			
			PUSH BX
			SUB BX,AX ; LOOK AGAIN
			CMP BX,2
