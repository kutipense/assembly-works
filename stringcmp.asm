MYSS SEGMENT PARA STACK 'STACK'
	DW 20 DUP(?)
MYSS ENDS

MYDS SEGMENT PARA 'DATA'
	DIZI1 DW 20 DUP(2)
	DIZI2 DW 15 DUP(2)
	ASD DW 2
	DW 4 DUP(2)
MYDS ENDS


MYCS SEGMENT PARA 'CODE'
	ASSUME SS:MYSS, CS:MYCS, DS:MYDS

MAIN PROC FAR
			PUSH DS
			XOR AX,AX
			PUSH AX
			MOV AX,MYDS
			MOV DS,AX
			MOV ES,AX

			MOV CX,20
			LEA SI,DIZI1
			LEA DI,DIZI2
			CLD
			REPE CMPSW

			RETF
MAIN ENDP

MYCS ENDS
	END MAIN
